#include <iostream>
#include <vector>

using namespace std;

class Solution {
public:
    int numTrees(int n) { // time: O(n^2); space: O(n)
        vector<int> dp(n + 1);
        dp[0] = 1;
        dp[1] = 1;
        for (int i = 2; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                dp[i] += dp[j - 1] * dp[i - j];
            }
        }
        return dp[n];
    }
};

// Test
int main () {
    Solution solution;
    cout << solution.numTrees(3) << endl;
    return 0;
}
